class RedRanger{
  constructor(type){
    //create red ranger
    console.log('created a new Red Power Ranger')
    this.color = type.color
    this.name = type.name
    this.weapon = type.weapon
    this.zord = type.zord

    console.log(`color: ${this.color}, name: ${this.name}, weapon: ${this.weapon}, zord: ${this.zord}`)
  }
}//End RedRanger


